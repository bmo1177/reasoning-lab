/**
 * Analyze digital traces to generate targeted what-questions.
 * UML: QuestionEngine.analyzeTraces()
 */
export function analyzeTraces(trace: DigitalTrace): string[] {
    const insights: string[] = [];

    // Analyze errors
    if (trace.errors.length > 0) {
        insights.push(`Detected ${trace.errors.length} errors.`);
    }

    // Analyze time
    if (trace.timeSpent > 120 && trace.errors.length === 0) {
        insights.push('Learner is calculating carefully.');
    } else if (trace.timeSpent < 10 && trace.errors.length > 0) {
        insights.push('Learner is rushing.');
    }

    return insights;
}

/**
 * Generate a structured WhatQuestion based on analysis.
 * UML: QuestionEngine.generateWhatQuestion()
 */
export function generateWhatQuestion(
    trace: DigitalTrace,
    context: QuestionContext
): WhatQuestion | null {
    // Example logic:
    // If learner made a semantic error, generate an "Evaluate" level question
    const hasSemanticError = trace.errors.some(e => e.includes('semantic'));

    if (hasSemanticError) {
        return {
            cognitiveLevel: 'evaluate',
            clinicalFocus: 'treatment_safety',
            hintLevel: 2,
            question: "What might be the physiological consequence of this intervention given the patient's low BP?",
            alternativeDecisionId: 'fluid_bolus_id' // Hypothetical
        };
    }

    return null;
}

// ======================
// Rule-Based Helpers
// ======================

function generateReflectionQuestion(
    decision: SimulationDecision,
    scenario: WhatIfScenario,
    context: QuestionContext,
): string {
    const questions: Record<string, string[]> = {
        better: [
            `Why might "${scenario.alternative_decision_label}" have led to a better outcome than "${decision.label}"?`,
            `What clinical reasoning supports choosing "${scenario.alternative_decision_label}" at this stage?`,
            `How would the patient's trajectory change if you had opted for "${scenario.alternative_decision_label}"?`,
        ],
        worse: [
            `What makes "${decision.label}" a better choice than "${scenario.alternative_decision_label}" in this context?`,
            `What risks are associated with "${scenario.alternative_decision_label}" for this patient?`,
            `Can you identify the clinical evidence that supports your decision of "${decision.label}"?`,
        ],
        similar: [
            `Both options could work here. What factors would help you distinguish between them?`,
            `In what clinical circumstances would "${scenario.alternative_decision_label}" become clearly preferable?`,
            `What additional information would help you confidently choose between these options?`,
        ],
    };

    const outcomeQuestions = questions[scenario.predicted_outcome] || questions['similar'];
    return outcomeQuestions[Math.floor(Math.random() * outcomeQuestions.length)];
}

function generateRuleBasedCausalChain(
    decision: SimulationDecision,
    scenario: WhatIfScenario,
    context: QuestionContext,
): CausalStep[] {
    const chain: CausalStep[] = [];
    const alt = scenario.alternative_decision_label || scenario.alternative_decision;

    // Step 1: Action taken
    chain.push({
        action: `Choose "${alt}" instead of "${decision.label}"`,
        consequence: scenario.predicted_outcome === 'better'
            ? 'May address the clinical priority more directly'
            : scenario.predicted_outcome === 'worse'
                ? 'Could miss a critical diagnostic or treatment step'
                : 'Provides similar clinical information',
        probability: scenario.predicted_outcome === 'similar' ? 'high' : 'medium',
    });

    // Step 2: Patient impact
    if (scenario.predicted_outcome === 'better') {
        chain.push({
            action: 'Earlier identification of key findings',
            consequence: 'Patient receives more targeted care sooner',
            probability: 'medium',
        });
    } else if (scenario.predicted_outcome === 'worse') {
        chain.push({
            action: 'Delayed or missed critical information',
            consequence: 'Risk of patient deterioration or delayed treatment',
            probability: 'medium',
        });
    }

    // Step 3: Learning outcome
    chain.push({
        action: 'Reflect on this decision point',
        consequence: 'Understanding helps improve future clinical reasoning',
        probability: 'high',
    });

    return chain;
}

// ======================
// LLM Helpers
// ======================

function buildLLMPrompt(
    decision: SimulationDecision,
    context: QuestionContext,
    existingScenarios: EnhancedWhatIfScenario[],
): string {
    const alts = existingScenarios.map(s => s.alternative_decision_label).join(', ');
    return `Generate enhanced what-if analysis for this clinical decision.

DECISION MADE: "${decision.label}" (${decision.type})
ALTERNATIVES CONSIDERED: ${alts || 'None identified'}
PATIENT STATUS: ${context.patientState.status}
STAGE: ${context.currentStage.name || context.currentStage.id}

For each alternative, provide:
1. A detailed causal chain (3-4 steps) showing how this choice affects patient outcome
2. A reflective question that promotes deeper clinical reasoning
3. A confidence score (0-1) for the predicted outcome

Return as JSON array of objects with: alternative_id, causal_chain [{action, consequence, probability}], reflection_question, confidence`;
}

function parseLLMResponse(
    data: any,
    decision: SimulationDecision,
    existingScenarios: EnhancedWhatIfScenario[],
): EnhancedWhatIfScenario[] {
    if (!data?.response) return [];

    try {
        const content = typeof data.response === 'string' ? data.response : JSON.stringify(data.response);
        const cleaned = content.replace(/```json\n?/g, '').replace(/```\n?/g, '').trim();
        const parsed = JSON.parse(cleaned);

        if (!Array.isArray(parsed)) return [];

        return parsed.map((item: any) => {
            const matching = existingScenarios.find(s => s.alternative_decision === item.alternative_id);
            return {
                ...(matching || existingScenarios[0]),
                causalChain: item.causal_chain || [],
                reflectionQuestion: item.reflection_question || '',
                source: 'llm' as const,
                confidence: item.confidence || 0.7,
            };
        }).filter(Boolean);
    } catch {
        return [];
    }
}

function mergeScenarios(
    ruleBased: EnhancedWhatIfScenario[],
    llm: EnhancedWhatIfScenario[],
): EnhancedWhatIfScenario[] {
    if (llm.length === 0) return ruleBased;

    // Merge: for each rule-based scenario, replace with LLM version if available
    return ruleBased.map(rb => {
        const llmVersion = llm.find(l => l.alternative_decision === rb.alternative_decision);
        if (llmVersion) {
            return {
                ...rb,
                causalChain: llmVersion.causalChain,
                reflectionQuestion: llmVersion.reflectionQuestion || rb.reflectionQuestion,
                source: 'llm' as const,
                confidence: llmVersion.confidence,
            };
        }
        return rb;
    });
}
