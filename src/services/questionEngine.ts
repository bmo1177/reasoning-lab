/**
 * Question Engine
 * 
 * Generates dynamic what-if questions from learner traces using a hybrid approach:
 * - Rule-based generation (instant, zero latency) as base
 * - LLM generation (async via Edge Function) to augment with causal reasoning chains
 * 
 * The rule-based layer always fires immediately. LLM responses are requested
 * asynchronously and replace/augment rule-based scenarios when they arrive.
 */

import type {
    SimulationDecision,
    BranchingCase,
    SimulationStage,
    PatientState,
} from '@/types/simulation';
import type { WhatIfScenario } from '@/types/analytics';
import type { DigitalTrace, WhatQuestion, CognitiveLevel } from '@/types/domainModel';
import { generateWhatIfScenarios } from '@/services/whatIfService';
import { supabase } from '@/integrations/supabase/client';

// ======================
// Types
// ======================

export interface CausalStep {
    action: string;
    consequence: string;
    probability: 'high' | 'medium' | 'low';
}

export interface EnhancedWhatIfScenario extends WhatIfScenario {
    /** Reasoning chain explaining why the alternative leads to this outcome */
    causalChain?: CausalStep[];
    /** Clinical reasoning question to prompt reflection */
    reflectionQuestion?: string;
    /** Whether this was generated by the LLM or rule engine */
    source: 'rule-based' | 'llm';
    /** Confidence score (0-1) for LLM-generated scenarios */
    confidence?: number;
}

export interface QuestionContext {
    caseData: BranchingCase;
    currentStage: SimulationStage;
    patientState: PatientState;
    decisionsLog: Array<{
        decision: SimulationDecision;
        timestamp: Date;
        stageId: string;
    }>;
    sessionId: string;
}

// ======================
// Cache for LLM responses
// ======================

const responseCache = new Map<string, { scenarios: EnhancedWhatIfScenario[]; timestamp: number }>();
const CACHE_TTL_MS = 60 * 60 * 1000; // 1 hour

function getCacheKey(decisionId: string, stageId: string): string {
    return `${decisionId}__${stageId}`;
}

function getCachedResponse(key: string): EnhancedWhatIfScenario[] | null {
    const entry = responseCache.get(key);
    if (entry && Date.now() - entry.timestamp < CACHE_TTL_MS) {
        return entry.scenarios;
    }
    responseCache.delete(key);
    return null;
}

function setCachedResponse(key: string, scenarios: EnhancedWhatIfScenario[]): void {
    responseCache.set(key, { scenarios, timestamp: Date.now() });
}

// ======================
// Core Functions
// ======================

/**
 * Generate enhanced what-if scenarios synchronously (rule-based).
 * Returns immediately with rule-based scenarios.
 */
export function generateRuleBasedScenarios(
    decision: SimulationDecision,
    context: QuestionContext,
): EnhancedWhatIfScenario[] {
    const baseScenarios = generateWhatIfScenarios(
        decision,
        context.caseData,
        context.currentStage,
    );

    return baseScenarios.map(scenario => ({
        ...scenario,
        source: 'rule-based' as const,
        reflectionQuestion: generateReflectionQuestion(decision, scenario, context),
        causalChain: generateRuleBasedCausalChain(decision, scenario, context),
    }));
}

/**
 * Generate LLM-enhanced scenarios asynchronously.
 * Falls back silently if the Edge Function is unavailable.
 */
export async function generateLLMScenarios(
    decision: SimulationDecision,
    context: QuestionContext,
    existingScenarios: EnhancedWhatIfScenario[],
): Promise<EnhancedWhatIfScenario[]> {
    const cacheKey = getCacheKey(decision.id, context.currentStage.id);
    const cached = getCachedResponse(cacheKey);
    if (cached) return cached;

    try {
        const { data, error } = await supabase.functions.invoke('eca-chat', {
            body: {
                message: buildLLMPrompt(decision, context, existingScenarios),
                conversationHistory: [],
                context: {
                    caseTitle: context.caseData.title,
                    specialty: context.caseData.specialty,
                    currentStage: context.currentStage.name || context.currentStage.id,
                    patientStatus: context.patientState.status,
                    vitalSigns: context.patientState.vitalSigns,
                    recentDecisions: context.decisionsLog.slice(-5).map(d => d.decision.label),
                    errorsDetected: [],
                    hintLevel: 0,
                    decisionsCount: context.decisionsLog.length,
                    timeElapsed: context.patientState.timeElapsed,
                },
                sessionId: context.sessionId,
            },
        });

        if (error) {
            console.warn('LLM scenario generation failed, using rule-based only:', error);
            return existingScenarios;
        }

        // Parse LLM response
        const llmScenarios = parseLLMResponse(data, decision, existingScenarios);
        const combined = mergeScenarios(existingScenarios, llmScenarios);

        setCachedResponse(cacheKey, combined);
        return combined;
    } catch (err) {
        console.warn('LLM scenario generation error:', err);
        return existingScenarios;
    }
}

/**
 * Full generation pipeline: rule-based first, then LLM async.
 * Returns rule-based immediately, calls onUpdate when LLM finishes.
 */
export function generateEnhancedScenarios(
    decision: SimulationDecision,
    context: QuestionContext,
    onUpdate: (scenarios: EnhancedWhatIfScenario[]) => void,
): EnhancedWhatIfScenario[] {
    // 1. Rule-based (instant)
    const ruleBasedScenarios = generateRuleBasedScenarios(decision, context);

    // 2. LLM (async, fire-and-forget)
    generateLLMScenarios(decision, context, ruleBasedScenarios)
        .then(enhanced => {
            if (enhanced !== ruleBasedScenarios) {
                onUpdate(enhanced);
            }
        })
        .catch(() => {
            // Silently fail — rule-based scenarios are sufficient
        });

    return ruleBasedScenarios;
}

